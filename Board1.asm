	MOVF    TUS_BASILDI_FLG, F
	BTFSS   STATUS, Z
	CALL    TUS_ISLE
	CLRF    TUS_BASILDI_FLG

	
	CALL    HABERLESME_KONTROL

	CALL    GECIKME_MS
	GOTO    ANA_DONGU

    
    ; SERI HABERLESME (PC KOMUTLARI)
    
    HABERLESME_KONTROL:
    BANKSEL PIR1
    BTFSS   PIR1, RCIF          
    RETURN                      

    BANKSEL RCREG
    MOVF    RCREG, W
    MOVWF   GELEN_VERI          
    BANKSEL PORTA

    
    
    MOVF    GELEN_VERI, W
    XORLW   0x01
    BTFSC   STATUS, Z
    GOTO    GONDER_ISTENEN_ONDA

    
    MOVF    GELEN_VERI, W
    XORLW   0x02
    BTFSC   STATUS, Z
    GOTO    GONDER_ISTENEN_TAM

    
    MOVF    GELEN_VERI, W
    XORLW   0x03
    BTFSC   STATUS, Z
    GOTO    GONDER_ORTAM_ONDA

    
    MOVF    GELEN_VERI, W
    XORLW   0x04
    BTFSC   STATUS, Z
    GOTO    GONDER_ORTAM_TAM

    
    MOVF    GELEN_VERI, W
    XORLW   0x05
    BTFSC   STATUS, Z
    GOTO    GONDER_FAN_HIZI

    
    MOVF    GELEN_VERI, W
    XORLW   0x06
    BTFSC   STATUS, Z
    GOTO    GONDER_ISTENEN_TAM      

    
    MOVF    GELEN_VERI, W
    ANDLW   b'11000000'
    XORLW   b'10000000'
    BTFSC   STATUS, Z
    GOTO    AYARLA_ISTENEN_ONDA

    
    MOVF    GELEN_VERI, W
    ANDLW   b'11000000'
    XORLW   b'11000000'
    BTFSC   STATUS, Z
    GOTO    AYARLA_ISTENEN_TAM

    RETURN

    
    GONDER_ISTENEN_ONDA:
	MOVF    ISTENEN_ONDA, W
	GOTO    UART_YOLLA
    GONDER_ISTENEN_TAM:
	MOVF    ISTENEN_TAM, W
	GOTO    UART_YOLLA
    GONDER_ORTAM_ONDA:
	MOVF    OLCULEN_ONDA, W
	GOTO    UART_YOLLA
    GONDER_ORTAM_TAM:
	MOVF    OLCULEN_TAM, W
	GOTO    UART_YOLLA
    GONDER_FAN_HIZI:
	MOVF    FAN_HIZI, W
	GOTO    UART_YOLLA

    UART_YOLLA:
	MOVWF   GIDEN_VERI
	BANKSEL TXREG
	MOVWF   TXREG
	BANKSEL PIR1
    BEKLE_TX:
