	CLRF    SANIYE_SAYAC
	CLRF    FAN_HIZI
	CLRF    TMR0
	CLRF    ESKI_ADC_DEGER

	MOVLW   d'50'
	MOVWF   ADC_SURE_SAY
	CLRF    TOPLAM_L
	CLRF    TOPLAM_H

	CALL    ADC_OKU
	CALL    ISI_KONTROL_MANTIGI

    
    
    
    ANA_DONGU:
	
	MOVF    AYAR_MODU, F
	BTFSS   STATUS, Z
	GOTO    AYAR_EKRANI_GOSTER

	
	DECFSZ  ADC_SURE_SAY, F
	GOTO    SICAKLIK_ISLEM_ATLA

	MOVLW   d'50'               
	MOVWF   ADC_SURE_SAY
	CALL    ADC_OKU
	CALL    ISI_KONTROL_MANTIGI

    SICAKLIK_ISLEM_ATLA:
	
	MOVF    EKRAN_DURUMU, W
	XORLW   0
	BTFSC   STATUS, Z
	CALL    HAZIRLA_ORTAM_SICAKLIK

	MOVF    EKRAN_DURUMU, W
	XORLW   1
	BTFSC   STATUS, Z
	CALL    HAZIRLA_ISTENEN_SICAKLIK

	MOVF    EKRAN_DURUMU, W
	XORLW   2
	BTFSC   STATUS, Z
	CALL    HAZIRLA_FAN_HIZI

	
	GOTO    TUS_KONTROL_NOKTASI

    AYAR_EKRANI_GOSTER:
	
	MOVF    GIRIS_ONLAR, W
	MOVWF   HAN1
	MOVF    GIRIS_BIRLER, W
	MOVWF   HAN2
	MOVF    GIRIS_ONDA, W
	MOVWF   HAN3
	MOVLW   d'11'               
	MOVWF   HAN4

    TUS_KONTROL_NOKTASI:
