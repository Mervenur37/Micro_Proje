	GOTO    BEKLE_TX
	BANKSEL PORTA
	RETURN

    ;  Ayarlama Islemleri
    AYARLA_ISTENEN_ONDA:
	MOVF    GELEN_VERI, W
	ANDLW   b'00111111'     
	MOVWF   ISTENEN_ONDA
	CALL    ISI_KONTROL_MANTIGI
	RETURN
    AYARLA_ISTENEN_TAM:
	MOVF    GELEN_VERI, W
	ANDLW   b'00111111'
	MOVWF   ISTENEN_TAM
	CALL    ISI_KONTROL_MANTIGI
	RETURN

   
    ; EKRAN ICIN SAYILAR
    
    HAZIRLA_ORTAM_SICAKLIK:
	CLRF    HAN1
	MOVF    OLCULEN_TAM, W
	MOVWF   HAN_GECICI
	CALL    BASAMAKLARI_AYIR
	MOVF    OLCULEN_ONDA, W
	MOVWF   HAN3
	MOVLW   d'10'           
	MOVWF   HAN4
	RETURN

    HAZIRLA_ISTENEN_SICAKLIK:
	CLRF    HAN1
	MOVF    ISTENEN_TAM, W
	MOVWF   HAN_GECICI
	CALL    BASAMAKLARI_AYIR
	MOVF    ISTENEN_ONDA, W
	MOVWF   HAN3
	MOVLW   d'11'           
	MOVWF   HAN4
	RETURN

    HAZIRLA_FAN_HIZI:
	CLRF    HAN1
	MOVF    FAN_HIZI, W
	MOVWF   HAN_GECICI
	CALL    BASAMAKLARI_AYIR
	CLRF    HAN3
	MOVLW   d'12'           
	MOVWF   HAN4
	RETURN

    BASAMAKLARI_AYIR:
    BOL_ONLAR:
	MOVLW   d'10'
	SUBWF   HAN_GECICI, W
	BTFSS   STATUS, C
	GOTO    BOLME_BITTI
	MOVWF   HAN_GECICI
	INCF    HAN1, F
	GOTO    BOL_ONLAR
    BOLME_BITTI:
	MOVF    HAN_GECICI, W
	MOVWF   HAN2
	RETURN

    
    ; EKRAN TARAMASI 
    
    EKRAN_YENILE:
	CLRF    PORTE               
	BCF     PORTA, 5
	MOVLW   HIGH TABLO_7SEG
	MOVWF   PCLATH

	MOVF    HAN_SAYACI, W
	ANDLW   0x03
	MOVWF   W_EKRAN_TEMP

	MOVF    W_EKRAN_TEMP, W
	BTFSC   STATUS, Z
	GOTO    HAN1_YAK
	MOVF    W_EKRAN_TEMP, W
	XORLW   1
	BTFSC   STATUS, Z
	GOTO    HAN2_YAK
	MOVF    W_EKRAN_TEMP, W
	XORLW   2
	BTFSC   STATUS, Z
	GOTO    HAN3_YAK
	GOTO    HAN4_YAK

    HAN1_YAK:
	MOVF    HAN1, W
	CALL    TABLO_7SEG
	MOVWF   PORTD
	MOVLW   b'00000001'
	MOVWF   PORTE
	GOTO    SONRAKI_HANE
    HAN2_YAK:
	MOVF    HAN2, W
	CALL    TABLO_7SEG
	IORLW   b'10000000'     
	MOVWF   PORTD
	MOVLW   b'00000010'
	MOVWF   PORTE
	GOTO    SONRAKI_HANE
    HAN3_YAK:
	MOVF    HAN3, W
	CALL    TABLO_7SEG
	MOVWF   PORTD
	MOVLW   b'00000100'
	MOVWF   PORTE
	GOTO    SONRAKI_HANE
    HAN4_YAK:
	MOVF    HAN4, W
	CALL    TABLO_7SEG
	MOVWF   PORTD
	BSF     PORTA, 5
	GOTO    SONRAKI_HANE
    SONRAKI_HANE:
	INCF    HAN_SAYACI, F
	CLRF    PCLATH
	RETURN

    
    ; ADC OKUMA + ORTALAMA ALMA
    
    ADC_OKU:
	CLRF    TOPLAM_L
	CLRF    TOPLAM_H
	MOVLW   d'32'           
	MOVWF   SAYAC_GENEL
    ADC_DONGUSU:
	BANKSEL ADCON0
	BSF     ADCON0, GO
    BEKLE_ADC:
	BTFSC   ADCON0, GO
	GOTO    BEKLE_ADC

	BANKSEL ADRESL
	MOVF    ADRESL, W
	BANKSEL TOPLAM_L
	ADDWF   TOPLAM_L, F
	BTFSC   STATUS, C
	INCF    TOPLAM_H, F
	DECFSZ  SAYAC_GENEL, F
	GOTO    ADC_DONGUSU

	
	MOVLW   d'5'
	MOVWF   KAYDIRMA_SAY
    KAYDIR_BOL:
	BCF     STATUS, C
	RRF     TOPLAM_H, F
	RRF     TOPLAM_L, F
	DECFSZ  KAYDIRMA_SAY, F
	GOTO    KAYDIR_BOL

	
	MOVF    TOPLAM_L, W
	SUBWF   ESKI_ADC_DEGER, W
	BTFSC   STATUS, Z
	GOTO    DEGISIM_YOK
	MOVF    ESKI_ADC_DEGER, W
	ADDLW   d'1'
	XORWF   TOPLAM_L, W
	BTFSC   STATUS, Z
	GOTO    DEGISIM_YOK
	MOVF    ESKI_ADC_DEGER, W
	ADDLW   d'255'
	XORWF   TOPLAM_L, W
	BTFSC   STATUS, Z
	GOTO    DEGISIM_YOK

	MOVF    TOPLAM_L, W
	MOVWF   ESKI_ADC_DEGER
	GOTO    DEGERI_HESAPLA
    DEGISIM_YOK:
	MOVF    ESKI_ADC_DEGER, W
	MOVWF   TOPLAM_L
    DEGERI_HESAPLA:
	
	MOVF    TOPLAM_L, W
	BANKSEL OLCULEN_TAM
	MOVWF   OLCULEN_TAM
	MOVLW   d'1'
	SUBWF   OLCULEN_TAM, F
	BCF     STATUS, C
	RRF     OLCULEN_TAM, F
	BTFSC   STATUS, C
	GOTO    BUCUK_VAR
	CLRF    OLCULEN_ONDA
	RETURN
    BUCUK_VAR:
	MOVLW   d'5'
	MOVWF   OLCULEN_ONDA
	RETURN

    ISI_KONTROL_MANTIGI:
	BANKSEL PORTC
	MOVF    ISTENEN_TAM, W
	SUBWF   OLCULEN_TAM, W
	BTFSS   STATUS, Z
	GOTO    TAM_KISIM_FARK
	MOVF    ISTENEN_ONDA, W
	SUBWF   OLCULEN_ONDA, W
	BTFSC   STATUS, C
	GOTO    SOGUTMA_AKTIF
	GOTO    ISITMA_AKTIF
    TAM_KISIM_FARK:
	BTFSC   STATUS, C
	GOTO    SOGUTMA_AKTIF
	GOTO    ISITMA_AKTIF
    SOGUTMA_AKTIF:
	BCF     PORTC, 0        ; Isitici Kapat
	BSF     PORTC, 1        ; Sogutucu Ac
	RETURN
    ISITMA_AKTIF:
	BSF     PORTC, 0        ; Isitici Ac
	BCF     PORTC, 1        ; Sogutucu Kapat
	RETURN

    
    ; Keypad Okuma
    
    KLAVYE_TARAMA:
	CLRF    TUS_BASILDI_FLG
	BANKSEL PORTB

	; 1. SUTUN
	MOVLW   b'11111110'
	MOVWF   PORTB
	CALL    GECIKME_AZ
	BTFSS   PORTB, 4
	GOTO    KONTROL_1
	BTFSS   PORTB, 5
	GOTO    KONTROL_2
	BTFSS   PORTB, 6
	GOTO    KONTROL_3
	BTFSS   PORTB, 7
	GOTO    KONTROL_A

	; 2. SUTUN
	MOVLW   b'11111101'
	MOVWF   PORTB
	CALL    GECIKME_AZ
	BTFSS   PORTB, 4
	GOTO    KONTROL_4
	BTFSS   PORTB, 5
	GOTO    KONTROL_5
	BTFSS   PORTB, 6
	GOTO    KONTROL_6
	BTFSS   PORTB, 7
	GOTO    KONTROL_B

	; 3. SUTUN
	MOVLW   b'11111011'
	MOVWF   PORTB
	CALL    GECIKME_AZ
	BTFSS   PORTB, 4
	GOTO    KONTROL_7
	BTFSS   PORTB, 5
	GOTO    KONTROL_8
	BTFSS   PORTB, 6
	GOTO    KONTROL_9
	BTFSS   PORTB, 7
	GOTO    KONTROL_C

	; 4. SUTUN
	MOVLW   b'11110111'
	MOVWF   PORTB
	CALL    GECIKME_AZ
	BTFSS   PORTB, 4
	GOTO    KONTROL_YILDIZ
	BTFSS   PORTB, 5
	GOTO    KONTROL_0
	BTFSS   PORTB, 6
	GOTO    KONTROL_KARE
	BTFSS   PORTB, 7
	GOTO    KONTROL_D
	RETURN

    ; Tus tanimlamalari
    KONTROL_1: CALL ARK_SONDURME
	       BTFSC PORTB, 4
	       RETURN
	       MOVLW d'1'
	       GOTO TUS_BULUNDU
    KONTROL_2: CALL ARK_SONDURME
	       BTFSC PORTB, 5
	       RETURN
	       MOVLW d'2'
	       GOTO TUS_BULUNDU
