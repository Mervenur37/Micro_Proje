	MOVLW   d'200'              
	SUBWF   SANIYE_SAYAC, W
	BTFSS   STATUS, C
	GOTO    KESME_CIKIS         

	
	CLRF    SANIYE_SAYAC

	
	MOVF    TMR0, W
	MOVWF   FAN_HIZI
	CLRF    TMR0                

	
	INCF    EKRAN_SURE, F
	BTFSS   EKRAN_SURE, 0       
	GOTO    KESME_CIKIS

	INCF    EKRAN_DURUMU, F
	MOVF    EKRAN_DURUMU, W
	XORLW   d'3'                
	BTFSC   STATUS, Z
	CLRF    EKRAN_DURUMU

    KESME_CIKIS:
	SWAPF   STATUS_YEDEK, W
	MOVWF   STATUS
	SWAPF   W_YEDEK, F
	SWAPF   W_YEDEK, W
	RETFIE

    
	ORG 0x0200
    TABLO_7SEG:
	ANDLW   0x0F
	ADDWF   PCL, F
