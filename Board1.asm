	RETLW   b'00000110' ; 1
	RETLW   b'01011011' ; 2
	RETLW   b'01001111' ; 3
	RETLW   b'01100110' ; 4
	RETLW   b'01101101' ; 5
	RETLW   b'01111101' ; 6
	RETLW   b'00000111' ; 7
	RETLW   b'01111111' ; 8
	RETLW   b'01101111' ; 9
	RETLW   b'00111001' ; C
	RETLW   b'01011110' ; d
	RETLW   b'01110001' ; F
	RETLW   b'00000000' ; Bos
	RETLW   b'01000000' ; -
	RETLW   b'01111001' ; E

    
	ORG 0x0300
    BASLA:
	
	BANKSEL ADCON1
