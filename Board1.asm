	       BTFSC PORTB, 7
	       RETURN
	       MOVLW 0x0A
	       GOTO TUS_BULUNDU
    KONTROL_4: CALL ARK_SONDURME
	       BTFSC PORTB, 4
	       RETURN
	       MOVLW d'4'
	       GOTO TUS_BULUNDU
    KONTROL_5: CALL ARK_SONDURME
	       BTFSC PORTB, 5
	       RETURN
	       MOVLW d'5'
	       GOTO TUS_BULUNDU
    KONTROL_6: CALL ARK_SONDURME
	       BTFSC PORTB, 6
	       RETURN
	       MOVLW d'6'
	       GOTO TUS_BULUNDU
    KONTROL_B: CALL ARK_SONDURME
	       BTFSC PORTB, 7
	       RETURN
	       MOVLW 0x0B
	       GOTO TUS_BULUNDU
    KONTROL_7: CALL ARK_SONDURME
	       BTFSC PORTB, 4
	       RETURN
	       MOVLW d'7'
	       GOTO TUS_BULUNDU
    KONTROL_8: CALL ARK_SONDURME
	       BTFSC PORTB, 5
	       RETURN
	       MOVLW d'8'
	       GOTO TUS_BULUNDU
    KONTROL_9: CALL ARK_SONDURME
	       BTFSC PORTB, 6
	       RETURN
	       MOVLW d'9'
	       GOTO TUS_BULUNDU
    KONTROL_C: CALL ARK_SONDURME
	       BTFSC PORTB, 7
	       RETURN
	       MOVLW 0x0C
	       GOTO TUS_BULUNDU
    KONTROL_YILDIZ: CALL ARK_SONDURME
	       BTFSC PORTB, 4
	       RETURN
	       MOVLW 0x0E       
	       GOTO TUS_BULUNDU
    KONTROL_0: CALL ARK_SONDURME
	       BTFSC PORTB, 5
	       RETURN
	       MOVLW d'0'
	       GOTO TUS_BULUNDU
    KONTROL_KARE: CALL ARK_SONDURME
	       BTFSC PORTB, 6
	       RETURN
	       MOVLW 0x0F       
	       GOTO TUS_BULUNDU
    KONTROL_D: CALL ARK_SONDURME
	       BTFSC PORTB, 7
	       RETURN
	       MOVLW 0x0D
	       GOTO TUS_BULUNDU

    TUS_BULUNDU:
	MOVWF   TUS_DEGERI
	MOVLW   1
	MOVWF   TUS_BASILDI_FLG

	
    BEKLE_BIRAK:
	MOVLW   b'11110000'      
	MOVWF   PORTB
	NOP
	MOVF    PORTB, W
	ANDLW   0xF0             
	XORLW   0xF0             
	BTFSS   STATUS, Z        
	GOTO    BEKLE_BIRAK

	CALL    ARK_SONDURME
	RETURN

    
    ; Tuslar
    
    TUS_ISLE:
	; A tusu ayar modunu acar
	MOVF    TUS_DEGERI, W
	XORLW   0x0A
	BTFSC   STATUS, Z
	GOTO    AYAR_BASLAT

	; Ayar modu aktif degilse diger tuslara bakma
	MOVF    AYAR_MODU, F
	BTFSC   STATUS, Z
	RETURN

	; Kare tusu onayla ve cik
	MOVF    TUS_DEGERI, W
	XORLW   0x0F
	BTFSC   STATUS, Z
	GOTO    AYAR_ONAYLA

	; Yildiz tusu virgul
	MOVF    TUS_DEGERI, W
	XORLW   0x0E
	BTFSC   STATUS, Z
	GOTO    VIRGULE_GEC

	; Sayi mi? (0-9)
	MOVF    TUS_DEGERI, W
	SUBLW   d'9'
	BTFSS   STATUS, C
	RETURN

	; Hangi basamaktayiz?
	MOVF    GIRIS_ADIM, W
	XORLW   0
	BTFSC   STATUS, Z
	GOTO    ONLAR_KAYDET
	MOVF    GIRIS_ADIM, W
	XORLW   1
	BTFSC   STATUS, Z
	GOTO    BIRLER_KAYDET
	MOVF    GIRIS_ADIM, W
	XORLW   3
	BTFSC   STATUS, Z
	GOTO    ONDA_KAYDET
	RETURN

    AYAR_BASLAT:
	MOVLW   1
	MOVWF   AYAR_MODU
	CLRF    GIRIS_ADIM
	CLRF    GIRIS_ONLAR
	CLRF    GIRIS_BIRLER
	CLRF    GIRIS_ONDA
	RETURN

    AYAR_ONAYLA:
	
	CLRF    W_EKRAN_TEMP
	 
	MOVF    GIRIS_ONLAR, F
	BTFSC   STATUS, Z
	GOTO    BIRLERI_EKLE
	 
	MOVF    GIRIS_ONLAR, W
	MOVWF   SAYAC_GENEL
    CARP_10:
	MOVLW   d'10'
	ADDWF   W_EKRAN_TEMP, F
	DECFSZ  SAYAC_GENEL, F
	GOTO    CARP_10

    BIRLERI_EKLE:
	MOVF    GIRIS_BIRLER, W
	ADDWF   W_EKRAN_TEMP, F 

	; Limit Kontrolleri
	
	MOVLW   d'10'
	SUBWF   W_EKRAN_TEMP, W
	BTFSS   STATUS, C
	GOTO    AYAR_IPTAL

	
	MOVF    W_EKRAN_TEMP, W
	SUBLW   d'50'
	BTFSS   STATUS, C
	GOTO    AYAR_IPTAL

	; Degerleri kaydet
